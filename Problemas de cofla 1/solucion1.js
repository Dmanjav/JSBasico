dineroCofla =  prompt("Cuanto dinero tienes Cofla?");

if (dineroCofla >= 0.6 && dineroCofla < 1) {
    alert("Puedes comprar el helado de awa");
}
else if (dineroCofla >= 1 && dineroCofla < 1.6) {
    alert("Puedes comprar el helado de crema");
}
else if (dineroCofla >= 1.6 && dineroCofla < 1.7) {
    alert("Puedes comprar el helado de heladix");
}
else if (dineroCofla >= 1.7 && dineroCofla < 1.8) {
    alert("Puedes comprar el helado de heladovich");
}
else if (dineroCofla >= 1.8 && dineroCofla < 2.9) {
    alert("Puedes comprar el helado de helardo");
}
else if (dineroCofla >= 2.9) {
    alert("Te puedes comprar el helado con confites o el bote de 1/4 de kg")
} else {
    alert("No te alcanza para nada conshesumadre")
}

